package domain.Pojos;

public class DonorReference {

	    private String id;
	    private String href;

	    public String getId() {
	        return id;
	    }

	    public DonorReference setId(String id) {
	        this.id = id;
	        return this;
	    }

	    public String getHref() {
	        return href;
	    }

	    public DonorReference setHref(String href) {
	        this.href = href;
	        return this;
	    }
	}

